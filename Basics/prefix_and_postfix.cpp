// Write a program to demonstrate difference between prefix and postfix.

#include<iostream>
using namespace std;
int main(){
    int a = 4;
    cout<<"The value of pre increment a is : "<<++a<<endl;
    cout<<"The value of post increment a is : "<<a++<<endl;
    return 0;
}


// output
// The value of pre increment a is : 5
// The value of post increment a is : 5